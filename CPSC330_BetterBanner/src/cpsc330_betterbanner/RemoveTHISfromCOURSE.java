    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String CUID;
        String Course = "ERROR";
        String FName = "ERROR";
        String LName = "ERROR";



        CUID = Version.RunSearch("SELECT UID FROM enrole WHERE UID= '" + Main.Start.getUID() + "' AND CRN= '" + jTextField2.getText() + "';");
        if(Integer.valueOf(CUID) ==Main.Start.getUID()){
            Course = Version.RunSearch("SELECT Name FROM course WHERE CRN= " + Integer.valueOf(jTextField2.getText()) + ";" );
            FName = Main.Start.getFName(); //Version.RunSearch("SELECT FName FROM users WHERE UID= "+ Integer.valueOf(Main.Start.getUID()) + ";" );
            LName = Main.Start.getLName(); //Version.RunSearch("SELECT LName FROM users WHERE UID= "+ Integer.valueOf(Main.Start.getUID()) + ";" );
            jTextArea1.setText("Remove Selected Student: \n" + FName + " " + LName + "\n from course \n" + Course + "");
            fail = false;
            jButton2.setText("GO");
        } else{
            jTextArea1.setText("Indicated Student is not currently \n enrolled in selected Course.");
            fail = true;
            jButton2.setText("Return to Main");
        }
}//GEN-LAST:event_jButton1ActionPerformed

