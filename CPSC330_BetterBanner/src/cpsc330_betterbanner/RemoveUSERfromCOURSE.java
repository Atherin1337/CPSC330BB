    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String CUID;
        String Course = "ERROR";
        String FName = "ERROR";
        String LName = "ERROR";

        CUID = Version.RunSearch("SELECT UID FROM enrole WHERE UID= '" + jTextField1.getText() + "' AND CRN= '" + jTextField2.getText() + "';");
        if(CUID.equals(jTextField1.getText())){
            Course = Version.RunSearch("SELECT Name FROM course WHERE CRN= " + Integer.valueOf(jTextField2.getText()) + ";" );           
            FName = Version.RunSearch("SELECT FName FROM users WHERE UID= "+ Integer.valueOf(jTextField1.getText()) + ";" );
            LName = Version.RunSearch("SELECT LName FROM users WHERE UID= "+ Integer.valueOf(jTextField1.getText()) + ";" );
            jTextArea1.setText("Remove Selected Student: \n" + FName + " " + LName + "\n from course \n" + Course + "");
            fail = false;
            jButton2.setText("GO");
        }
        else{
             jTextArea1.setText("Indicated Student is not currently \n enrolled in selected Course.");
             fail = true;
             jButton2.setText("Return to Main");
        }
}//GEN-LAST:event_jButton1ActionPerformed

